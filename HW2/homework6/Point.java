
package homework6;


public class Point {
    // Declare two public double attributes called X and Y
    public double X;
    public double Y;

    // Define a default constructor that sets X and Y to zero
    public Point() {
        X = 0.0;
        Y = 0.0;
    }

    // Define a parameterized constructor that assigns given values to X and Y
    public Point(double x, double y) {
        X = x;
        Y = y;
    }
}
